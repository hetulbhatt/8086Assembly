DATA SEGMENT 
	N DW 06H
	ANS DW ?
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA, SS:MYSTACK
	MOV AX,DATA
	MOV DS,AX
	MOV AX,MYSTACK
	MOV SS,AX
	LEA SP,TOP

	MOV DX,N
	MOV AX,1
	CALL FACT
	INT 03H

	FACT PROC
		CMP DX,0
		JE OUT1
		PUSH DX
		DEC DX
		CALL FACT
		POP DX
		MUL DX
		OUT1:	RET
	FACT ENDP
CODE ENDS

MYSTACK SEGMENT
	DW 32 DUP('A')
	TOP LABEL WORD
MYSTACK ENDS
END